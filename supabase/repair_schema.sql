create extension if not exists pgcrypto;

do $$
begin
  if not exists (
    select 1
    from pg_type
    where typname = 'wallpaper_status'
  ) then
    create type public.wallpaper_status as enum ('processing', 'published', 'rejected');
  end if;
end
$$;

do $$
begin
  if not exists (
    select 1
    from pg_type
    where typname = 'wallpaper_variant'
  ) then
    create type public.wallpaper_variant as enum ('original', '4k', 'thumb', 'preview');
  end if;
end
$$;

do $$
declare
  users_id_type text;
  wallpapers_id_type text;
  legacy_wallpaper_column text;
begin
  create table if not exists public.users (
    id uuid primary key default gen_random_uuid(),
    email text unique,
    username text not null unique,
    avatar_url text,
    bio text,
    created_at timestamptz not null default now()
  );

  if exists (
    select 1
    from information_schema.tables
    where table_schema = 'public'
      and table_name = 'users'
  ) then
    alter table public.users add column if not exists email text;
    alter table public.users add column if not exists username text;
    alter table public.users add column if not exists avatar_url text;
    alter table public.users add column if not exists bio text;
    alter table public.users add column if not exists created_at timestamptz not null default now();

    create unique index if not exists users_username_key on public.users (username);
    create unique index if not exists users_email_key on public.users (email) where email is not null;
  end if;

  select format_type(a.atttypid, a.atttypmod)
    into users_id_type
  from pg_attribute a
  join pg_class c on c.oid = a.attrelid
  join pg_namespace n on n.oid = c.relnamespace
  where n.nspname = 'public'
    and c.relname = 'users'
    and a.attname = 'id'
    and not a.attisdropped;

  if not exists (
    select 1
    from information_schema.tables
    where table_schema = 'public'
      and table_name = 'wallpapers'
  ) then
    execute format(
      'create table public.wallpapers (
        id bigint generated by default as identity primary key,
        user_id %s references public.users(id) on delete set null,
        title text not null,
        slug text not null unique,
        description text,
        status public.wallpaper_status not null default ''processing'',
        tags text[] not null default ''{}'',
        colors text[] not null default ''{}'',
        width integer,
        height integer,
        downloads_count integer not null default 0,
        likes_count integer not null default 0,
        featured boolean not null default false,
        created_at timestamptz not null default now(),
        updated_at timestamptz not null default now()
      )',
      users_id_type
    );
  end if;

  execute format('alter table public.wallpapers add column if not exists user_id %s', users_id_type);
  alter table public.wallpapers add column if not exists title text;
  alter table public.wallpapers add column if not exists slug text;
  alter table public.wallpapers add column if not exists description text;
  alter table public.wallpapers add column if not exists status public.wallpaper_status not null default 'processing';
  alter table public.wallpapers add column if not exists tags text[] not null default '{}';
  alter table public.wallpapers add column if not exists ai_tags text[] not null default '{}';
  alter table public.wallpapers add column if not exists ai_category text;
  alter table public.wallpapers add column if not exists ai_caption text;
  alter table public.wallpapers add column if not exists ai_provider text;
  alter table public.wallpapers add column if not exists ai_model text;
  alter table public.wallpapers add column if not exists ai_analysis_status text not null default 'skipped';
  alter table public.wallpapers add column if not exists ai_analysis_error text;
  alter table public.wallpapers add column if not exists ai_analyzed_at timestamptz;
  alter table public.wallpapers add column if not exists colors text[] not null default '{}';
  alter table public.wallpapers add column if not exists width integer;
  alter table public.wallpapers add column if not exists height integer;
  alter table public.wallpapers add column if not exists downloads_count integer not null default 0;
  alter table public.wallpapers add column if not exists likes_count integer not null default 0;
  alter table public.wallpapers add column if not exists featured boolean not null default false;
  alter table public.wallpapers add column if not exists created_at timestamptz not null default now();
  alter table public.wallpapers add column if not exists updated_at timestamptz not null default now();

  if exists (
    select 1
    from information_schema.columns
    where table_schema = 'public'
      and table_name = 'wallpapers'
      and column_name = 'category_id'
      and is_nullable = 'NO'
  ) then
    alter table public.wallpapers alter column category_id drop not null;
  end if;

  if exists (
    select 1
    from information_schema.columns
    where table_schema = 'public'
      and table_name = 'wallpapers'
      and column_name = 'url'
      and is_nullable = 'NO'
  ) then
    alter table public.wallpapers alter column url drop not null;
  end if;

  if exists (
    select 1
    from information_schema.columns
    where table_schema = 'public'
      and table_name = 'wallpapers'
      and column_name = 'preview_url'
      and is_nullable = 'NO'
  ) then
    alter table public.wallpapers alter column preview_url drop not null;
  end if;

  if exists (
    select 1
    from information_schema.columns
    where table_schema = 'public'
      and table_name = 'wallpapers'
      and column_name = 'thumbnail_url'
      and is_nullable = 'NO'
  ) then
    alter table public.wallpapers alter column thumbnail_url drop not null;
  end if;

  if exists (
    select 1
    from information_schema.columns
    where table_schema = 'public'
      and table_name = 'wallpapers'
      and column_name = 'image_url'
      and is_nullable = 'NO'
  ) then
    alter table public.wallpapers alter column image_url drop not null;
  end if;

  if exists (
    select 1
    from information_schema.columns
    where table_schema = 'public'
      and table_name = 'wallpapers'
      and column_name = 'original_url'
      and is_nullable = 'NO'
  ) then
    alter table public.wallpapers alter column original_url drop not null;
  end if;

  if exists (
    select 1
    from information_schema.columns
    where table_schema = 'public'
      and table_name = 'wallpapers'
      and column_name = 'file_url'
      and is_nullable = 'NO'
  ) then
    alter table public.wallpapers alter column file_url drop not null;
  end if;

  for legacy_wallpaper_column in
    select c.column_name
    from information_schema.columns c
    where c.table_schema = 'public'
      and c.table_name = 'wallpapers'
      and c.is_nullable = 'NO'
      and (
        c.column_name like '%url%'
        or c.column_name like '%thumbnail%'
        or c.column_name in (
          'thumbnail',
          'thumb',
          'image',
          'cover',
          'cover_image',
          'hero_image',
          'image_path',
          'file_path',
          'src'
        )
      )
  loop
    execute format(
      'alter table public.wallpapers alter column %I drop not null',
      legacy_wallpaper_column
    );
  end loop;

  create unique index if not exists wallpapers_slug_key on public.wallpapers (slug);
  create index if not exists wallpapers_status_created_idx on public.wallpapers (status, created_at desc);
  create index if not exists wallpapers_featured_idx on public.wallpapers (featured) where featured = true;
  create index if not exists wallpapers_tags_gin_idx on public.wallpapers using gin (tags);
  create index if not exists wallpapers_ai_analysis_status_idx on public.wallpapers (ai_analysis_status);

  execute '
    update public.wallpapers w
    set user_id = null
    where w.user_id is not null
      and not exists (
        select 1
        from public.users u
        where u.id = w.user_id
      )
  ';

  if not exists (
    select 1
    from pg_constraint
    where conname = 'wallpapers_user_id_fkey'
      and conrelid = 'public.wallpapers'::regclass
  ) then
    execute 'alter table public.wallpapers add constraint wallpapers_user_id_fkey foreign key (user_id) references public.users(id) on delete set null';
  end if;

  select format_type(a.atttypid, a.atttypmod)
    into wallpapers_id_type
  from pg_attribute a
  join pg_class c on c.oid = a.attrelid
  join pg_namespace n on n.oid = c.relnamespace
  where n.nspname = 'public'
    and c.relname = 'wallpapers'
    and a.attname = 'id'
    and not a.attisdropped;

  if not exists (
    select 1
    from information_schema.tables
    where table_schema = 'public'
      and table_name = 'wallpaper_files'
  ) then
    execute format(
      'create table public.wallpaper_files (
        id bigint generated by default as identity primary key,
        wallpaper_id %s not null references public.wallpapers(id) on delete cascade,
        variant public.wallpaper_variant not null,
        storage_path text not null,
        url text not null,
        size bigint,
        format text,
        width integer,
        height integer,
        created_at timestamptz not null default now(),
        unique (wallpaper_id, variant)
      )',
      wallpapers_id_type
    );
  end if;

  execute format('alter table public.wallpaper_files add column if not exists wallpaper_id %s', wallpapers_id_type);
  alter table public.wallpaper_files add column if not exists variant public.wallpaper_variant;
  alter table public.wallpaper_files add column if not exists storage_path text;
  alter table public.wallpaper_files add column if not exists url text;
  alter table public.wallpaper_files add column if not exists size bigint;
  alter table public.wallpaper_files add column if not exists format text;
  alter table public.wallpaper_files add column if not exists width integer;
  alter table public.wallpaper_files add column if not exists height integer;
  alter table public.wallpaper_files add column if not exists created_at timestamptz not null default now();

  create unique index if not exists wallpaper_files_wallpaper_id_variant_key
    on public.wallpaper_files (wallpaper_id, variant);
  create index if not exists wallpaper_files_wallpaper_id_idx
    on public.wallpaper_files (wallpaper_id);

  execute '
    delete from public.wallpaper_files wf
    where not exists (
      select 1
      from public.wallpapers w
      where w.id = wf.wallpaper_id
    )
  ';

  if not exists (
    select 1
    from pg_constraint
    where conname = 'wallpaper_files_wallpaper_id_fkey'
      and conrelid = 'public.wallpaper_files'::regclass
  ) then
    execute 'alter table public.wallpaper_files add constraint wallpaper_files_wallpaper_id_fkey foreign key (wallpaper_id) references public.wallpapers(id) on delete cascade';
  end if;
end
$$;

alter table if exists public.users enable row level security;
alter table if exists public.wallpapers enable row level security;
alter table if exists public.wallpaper_files enable row level security;

do $$
begin
  if exists (
    select 1
    from information_schema.tables
    where table_schema = 'public'
      and table_name = 'users'
  ) then
    drop policy if exists "Public users are readable" on public.users;
    create policy "Public users are readable"
    on public.users
    for select
    using (true);
  end if;

  if exists (
    select 1
    from information_schema.tables
    where table_schema = 'public'
      and table_name = 'wallpapers'
  ) then
    drop policy if exists "Published wallpapers are readable" on public.wallpapers;
    create policy "Published wallpapers are readable"
    on public.wallpapers
    for select
    using (status = 'published');
  end if;

  if exists (
    select 1
    from information_schema.tables
    where table_schema = 'public'
      and table_name = 'wallpaper_files'
  ) then
    drop policy if exists "Published wallpaper files are readable" on public.wallpaper_files;
    create policy "Published wallpaper files are readable"
    on public.wallpaper_files
    for select
    using (
      exists (
        select 1
        from public.wallpapers
        where public.wallpapers.id = public.wallpaper_files.wallpaper_id
          and public.wallpapers.status = 'published'
      )
    );
  end if;
end
$$;
